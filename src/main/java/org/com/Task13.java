package org.com;

public class Task13 {
    public static void main(String[] args) {
        //13. Using the for loop, display even numbers from 1 to 100 inclusive.
        // Use a space or a new line.

        for (int i = 1; i <= 100; i++) {
            if (i % 2 == 0)
                System.out.print(i + " ");
        }
    }
}
