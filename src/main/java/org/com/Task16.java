package org.com;

public class Task16 {
    public static void main(String[] args) {
        //16. Using the for loop, print to the screen:
        // horizontal line of 10 eights,
        // a vertical line of 10 eights.

        System.out.println("Horizontal line of 10 eights: ");
        for (int k = 1; k <= 10; k++){
                System.out.print("8");
            }
        System.out.println();
        System.out.println("Vertical line of 10 eights: ");
        for (int k = 1; k <= 10; k++){
            System.out.println("8");
        }
    }
}
